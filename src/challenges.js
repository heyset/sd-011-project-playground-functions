function footballPoints(wins, ties) {
  let totalPoints = (wins * 3) + (ties * 1);
  return totalPoints;
}

// Desafio 6
function highestCount(numArray) {
  let maxNumber = Math.max(...numArray);
  let count = 0;
  for (let index = 0; index < numArray.length; index += 1) {
    if (numArray[index] === maxNumber) {
      count += 1;
    }
  }
  return count;
}

