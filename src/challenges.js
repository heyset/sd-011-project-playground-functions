// Desafio 6
function highestCount(array) {
  let maior = 0;
  for (let i in array){
    if (array[i] > maior) {
      maior = array[i];
    }
  }
  let counter = 0;
  for (let n in array){
    if (array[n] === maior) {
      counter += 1;
    }
  }
  return counter;
}

